package Figuras;

public class Puntos {
    private int punto_x;
    private int punto_y;

    public Puntos(int punto_x, int punto_y) {
        this.punto_x = punto_x;
        this.punto_y = punto_y;
    }

    public int getPunto_x() {
        return punto_x;
    }

    public void setPunto_x(int punto_x) {
        this.punto_x = punto_x;
    }

    public int getPunto_y() {
        return punto_y;
    }

    public void setPunto_y(int punto_y) {
        this.punto_y = punto_y;
    }
}
